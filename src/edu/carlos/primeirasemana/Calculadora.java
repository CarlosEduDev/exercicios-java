package edu.carlos.primeirasemana;

/**
<h1>Calculadora</h1>
A calculadora realiza operações matemáticas entre números inteiros
@author Carlos Eduardo
@version 1.0
@since 24/04/2024
*
* */
public class Calculadora {

    /**
    * Este método é utilizado para somar dois números inteiros
    * @param numero1 este é o primeiro parâmetro do método
    * @param numero2 este é o segundo parâmetro do método
     * @return int o resultado deste método é a soma dos dois números(parâmetros)
    * */
    public int somar(int numero1, int numero2){
        return numero1 + numero2;
    }

}
